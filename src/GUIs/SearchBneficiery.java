    private void searchDonorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchDonorActionPerformed
        // TODO add your handling code here:
        String dName = this.beneficieryName.getText();
        Beneficiery beneficiery = dms.searchBeneficiery(dName);
        if(beneficiery != null) {
            
            dlm.clear();
            dlm.addElement("Beneficiery ID: " + beneficiery.getId());
            dlm.addElement("Beneficiery Name: " + beneficiery.getName());
            dlm.addElement("beneficiery CNIC: " + beneficiery.getCnic());
            dlm.addElement("Beneficiery Address: " + beneficiery.getAddress());
            dlm.addElement("Total Funding: " + dms.showTotalFundings(dName));
        }

        arr = dms.searchProjectsOfBeneficiery(dName);

        if(arr != null ) {
            
            dlm1.clear();
            for(int i=0; i<arr.size(); ++i) {
                dlm1.addElement(arr.get(i).getName());
            }
        }

    }//GEN-LAST:event_searchDonorActionPerformed

