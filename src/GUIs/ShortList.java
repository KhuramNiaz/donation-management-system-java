    public void setApplicants() {
        details.setModel(dlm1);
        dlm1.clear();
        applicants.setModel(dlm);
        dlm.clear();
        for(int i=0; i < dms.getApplicants().size(); ++i) {
            dlm.addElement(dms.getApplicants().get(i).getName());
        }
    }
    
