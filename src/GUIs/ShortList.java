    private void rejectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rejectActionPerformed
        // TODO add your handling code here:
        String name = this.name.getText();
        dms.removeApplicant(name);
        dlm.clear();
        dlm1.clear();
        setApplicants();
    }//GEN-LAST:event_rejectActionPerformed

    private void viewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewActionPerformed
        // TODO add your handling code here:
        String name = this.name.getText();
        Beneficiery applicant = dms.searchApplicant(name);
        if(applicant != null) {
            dlm1.addElement("ID: " + applicant.getId());
            dlm1.addElement("Name: " + applicant.getName());
            dlm1.addElement("CNIC: " + applicant.getCnic());
            dlm1.addElement("Address: " + applicant.getAddress());
            dlm1.addElement("Salary: " + applicant.getSalary());
        }
    }//GEN-LAST:event_viewActionPerformed

